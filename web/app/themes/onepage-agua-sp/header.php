    <h1>Em meio à pior crise hídrica da história, governos propõem criação de "indústria da seca" em SP</h1>

    <h2>Aliança da Água critica pacote bilionário de obras que não apresenta plano de emergência, não ataca as causas da falta d’água nem garante níveis seguros dos reservatórios para o próximo período de estiagem.</h2>

    <p><strong>São Paulo, 12 de novembro de 2014</strong> - Desde o início deste ano, os indícios de que São Paulo viveria a pior crise hídrica de sua história eram claros. Com medo da reação da opinião pública durante o período eleitoral, os governos adiaram medidas de contenção. A inação política agravou o cenário de estiagem. Mais de 60 municípios já enfrentam cortes no fornecimento e o racionamento atinge milhões de pessoas.</p>

    <p>Passadas duas semanas do fim da eleições, na última segunda-feira, dia 10/11, o governador Geraldo Alckmin por fim reuniu-se com a presidenta Dilma Rousseff em Brasília para pedir apoio a um pacote bilionário de obras hídricas no Estado de São Paulo. As medidas propostas, como a interligação dos reservatórios e a construção de novas represas, embora aparentemente "volumosas", falham em responder à crise, não garantem transparência e participação da sociedade e não criam mecanismos robustos para promover a segurança hídrica do Estado. </p>

    <p>Especialistas em recursos hídricos e mudanças climáticas apontam quatro fatores que deram origem à crise: a) ênfase dos governos na retirada de mais água, e não no uso racional desse recurso; b) desmatamento nas áreas de mananciais e poluição das fontes de água em quase todo o Estado; c) seca extrema e déficit de chuvas, em especial  no Sistema Cantareira;  d) pouco espaço de participação social e transparência na gestão da água. </p>

    <p>A Aliança pela Água -- rede de entidades e coletivos da sociedade civil que se organizou em outubro para monitorar as respostas do poder público e propor soluções de curto, médio e longo prazos para superar a crise-- alerta que não existe mais tempo, dinheiro e água para desperdiçar com um conjunto de obras que não vai resolver o problema e ressalta que as propostas apresentadas não atacam suas causas. </p>

    <p>De acordo com a Aliança, adotar esse caminho é insistir no erro: novas e caras obras que não cuidam das nascentes, não recuperam áreas já exploradas e não reduzem consumo e perdas, repetindo o padrão histórico insustentável. Além disso, a prioridade deve ser a apresentação e discussão pública de um plano de contigência que indique como chegar em abril de 2015 em situação segura para garantir o fornecimento durante o próximo período de estiagem.</p>

    <p>Outro ponto que chama a atenção no pacote de medidas discutido por Dilma e Alckmin é a ausência de qualquer menção sobre a recuperação e recomposição dos mananciais existentes - como  a restauração florestal, a ampliação de parques e áreas protegidas, a contenção da expansão urbana sobre áreas de mananciais, além de instrumentos econômicos para promover essas ações. Tampouco se considera os impactos da expansão urbana, do desmatamento e das mudanças climáticas nas mudanças do regime de chuvas.</p>

    <p>O sentido de urgência e a escala do problema são claros: o mês de outubro foi um dos mais secos já registrados e as previsões climáticas para os próximos meses não são animadoras. Os dados indicam que, se chover como em 2013, o colapso é praticamente certo; e, mesmo que chova dentro da média, os mananciais ainda se encontrarão em situação pior do que estavam em abril de 2014. </p>

    <p>Por essas razões, a Aliança da Água cobra a apresentação e discussão imediata de um plano de contingência claro e que priorize a transparência dos dados e da real situação do abastecimento de água no Estado. As organizações da sociedade civil, movimentos, coletivos e pessoas que a compõem defendem que qualquer investimento público em obras de novas transposições e de grande porte deve estar condicionado a plano de sustentabilidade que garanta segurança hídrica e que considere variáveis sociais e climáticas antes de implementar tal infraestrutura, reforçando os instrumentos já estabelecidos pela Lei Nacional de Recursos Hídricos (lei 7.663/1991). No curto e médio prazo, e de forma permanente, é preciso que sejam implantadas ações para diminuir consumo em diferentes escalas, como programas de economia, reuso, captação de água de chuva e redução nas perdas do sistema. </p>

    <p>Impõe-se a necessidade de difusão permanente de informações sobre a crise, para que a população possa enfrentá-la com maior segurança, evitando a proliferação de poços potencialmente contaminados e a reservação inadequada de água. A crise hídrica provavelmente se estenderá por muitos anos e a pouca disponibilidade de água na região metropolitana é uma realidade a ser enfrentada com coragem e gestão cuidadosa. Não basta buscar aumentar a oferta, sem cuidar da demanda. </p>


    <p><strong>Crítica aos investimentos anunciados: </strong></p>

    <ul class="lista">
      <li><p>O conjunto de intervenções apresentado não resolve a crise atual, é fazer mais do mesmo, ou seja, novas e caras obras que não contemplam medidas estratégicas para criar segurança hídrica. </p></li>
      <li><p>Até o momento não foi apresentado um plano de contingência que demonstre como vamos chegar em abril de 2015 em situação segura para encarar o próximo período de estiagem.</p></li>
      <li><p>Não foi feita qualquer menção sobre recuperar e cuidar dos mananciais existentes (restauração florestal, ampliação de parques, pagamentos por serviços ambientais)</p></li>
    </ul>

    <p><strong>O que queremos:</strong></p>

    <ul class="lista">
      <li><p>A apresentação e discussão imediata de um plano de contingência transparente, que permita que a sociedade se organize e se prepare para a crise continuada e o próximo período de estiagem.</p></li>
      <li><p>Que sejam implantadas ações imediatas com metas de redução de consumo e de perdas para diferentes tipos de usuários de água, como agricultura, indústria, concessionárias de serviços de saneamento e grandes consumidores urbanos.</p></li>
      <li><p>Que todo e qualquer investimento para novas transposições e obras desse porte estejam condicionados a um plano abrangente de segurança hídrica e recuperação das fontes de água existentes, considerando-se variáveis sociais e climáticas, para garantir um futuro seguro e sustentável para a água em São Paulo. </p></li>
    </ul>

